package com.example.myapplication44;

import android.graphics.Color;

public class Choise {
    private final char X='X' ;
    private final char O= 'O';
    private Color x ;
    private Color y ;

    public Choise() {
        this.x = x;
        this.y = y;
    }

    public char getX() {
        return X;
    }

    public void setX(Color x) {
        this.x = x;
    }

    public Color getY() {
        return y;
    }

    public void setY(Color y) {
        this.y = y;
    }

    public char getO() {
        return O;
    }
}
